    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(count == 1){
        int d = c.getIdCliente();
        c.setIdCliente(d+1);
        c.setNome(Cnome.getText());
        c.setEmail(Cemail.getText());
        c.setTelefone(Ctelefone.getText());
        c.setRua(Crua.getText());
        c.setNumero(Cnumero.getText());
        cd.incluir(c);
        CadastroComputador comp = new CadastroComputador();
        TelaInicial t = TelaInicial.getInstancia();
        t.adicionarInternalFrame(comp);
        this.dispose();
        }
        if(count==2){
            int id = Integer.parseInt(txtID.getText());
            compDAO.removerFK(id);
            cd.remover(id);
        }

    }//GEN-LAST:event_jButton2ActionPerformed

